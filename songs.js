document.addEventListener('click', e => {
  const item = e.target.closest('.playlist--song');
  if (!item) return;
  const audio = document.querySelector('#audio');
  const index = Number(item.dataset.index);
  const song = window.SONGS[index];
  if (song) {
    audio.src = song.path;
    audio.play().catch(() => {}); // tránh lỗi autoplay trên mobile
    const title = document.querySelector('.header h2');
    const thumb = document.querySelector('.cd--thumb');
    if (title) title.textContent = song.name;
    if (thumb) thumb.style.backgroundImage = `url('${song.image}')`;
  }
});
